def load_srt(path: Path) -> dict[int, dict[str, object]]:
    text = path.read_text(encoding="utf-8").strip()
    entries: dict[int, dict[str, object]] = {}
    for block in text.split("\n\n"):
        lines = block.splitlines()
        idx = int(lines[0])
        entries[idx] = {
            "index": idx,
            "timecode": lines[1],
            "text": lines[2:],
        }
    return entries


def normalize_lines(lines: list[str]) -> list[str]:
    normalized: list[str] = []
    for line in lines:
        line = (
            line.replace("？", "?")
            .replace("，", ",")
            .replace("“", '"')
            .replace("”", '"')
            .replace("‘", "'")
            .replace("’", "'")
        )
        line = line.replace("Hai Gui", "Hai Guai").replace("Hai guai", "Hai Guai")
        line = line.replace("konw", "know").replace("experence", "experience")
        line = re.sub(r"(?<![A-Za-z])i(?![A-Za-z])", "I", line)
        line = re.sub(r"\s{2,}", " ", line).strip()
        normalized.append(line)
    return normalized


